#! /user/bin/env python
# encoding: utf-8


class Calc:
    def add(self, a, b):
        c = a + b
        return c

    def minus(self, a, b):
        c = a - b
        return c

    def mul(self, a, b):
        c = a * b
        return c

    def div(self, a, b):
        c = a / b
        return c
